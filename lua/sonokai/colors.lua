local colors = {}

---@param style string
---@param colors_override table<string, string[]>|nil
function colors.generate(style, colors_override)
  local palette = vim.deepcopy(palettes[style] or palettes.default)
  if colors_override then
    for key, value in pairs(colors_override) do
      palette[key] = value
    end
  end

  for key, value in pairs(palette) do
    colors[key] = value[1]
  end

  return colors
end

